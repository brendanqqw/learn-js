//OR Operator
//Either CONDITIONS SHOULD BE TRUE

let hasDegree = true;
let experience = 3;

console.log(hasDegree || experience > 2);
//Output: true
//Explanation: hasDegree is true, so the condition is true.

console.log(hasDegree || experience > 4);
//Output: true
//Explanation: hasDegree is true, so the condition is true.
